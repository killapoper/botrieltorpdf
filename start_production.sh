#!/bin/bash

# Скрипт для запуска обоих сервисов на продакшене
# Используется в Coolify или на VDS

echo "=========================================="
echo "Запуск Telegram бота и веб-сервера"
echo "=========================================="
echo ""

# Проверка переменных окружения
if [ -z "$TELEGRAM_BOT_TOKEN" ]; then
    echo "❌ ОШИБКА: TELEGRAM_BOT_TOKEN не установлен!"
    exit 1
fi

echo "✅ Переменные окружения проверены"
echo ""

# Создание папки для данных
mkdir -p data
echo "✅ Папка data создана"
echo ""

# Запуск бота в фоновом режиме
echo "🤖 Запуск Telegram бота..."
python bot.py &
BOT_PID=$!
echo "✅ Бот запущен (PID: $BOT_PID)"
echo ""

# Небольшая пауза
sleep 2

# Запуск веб-сервера (основной процесс)
echo "🌐 Запуск веб-сервера на порту 5000..."
python web_server.py

# Если веб-сервер упал, убиваем бота
kill $BOT_PID 2>/dev/null
